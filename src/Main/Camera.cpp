void makeGaussianFilter(double Q)
{
	int i, j;
	double sum = 0.0;
	for(j=-1; j<=1; j++)
		for(i=-1; i<=1; i++)
		{
			double flt = exp(-(i*i+j*j)/(Q*Q));
			sum += flt;
			G_FILTER[i+1][j+1] = flt;
		}

	for(j=-1; j<=1; j++)
		for(i=-1; i<=1; i++)
		{
			G_FILTER[i+1][j+1] = G_FILTER[i+1][j+1]/sum;
		}
}

