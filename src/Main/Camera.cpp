void CCamera::Move(GLdouble x, GLdouble y, GLdouble z){
	GLfloat modelView[16];

	glPushMatrix();

	glLoadIdentity();
	glTranslated(-y, z, x );

	glGetFloatv(GL_MODELVIEW_MATRIX, modelView);

	glLoadIdentity();
	glRotatef( tilt, -1.0f, 0.0f, 0.0f );
	glRotatef( pan,  0.0f, -1.0f, 0.0f );
	glRotatef( roll, 0.0f, 0.0f, 1.0f );

	glMultMatrixf(modelView);

	glGetFloatv(GL_MODELVIEW_MATRIX, modelView);

	position[0] -= modelView[3*4+0];
	position[1] -= modelView[3*4+1];
	position[2] += modelView[3*4+2];


	glPopMatrix();

}

void CCamera::Move(int dir, dBodyID target)
{
	if (target == 0)return;

	const dReal* pos = dBodyGetPosition(target);

	float dx = position[0] - pos[0];
	float dy = position[1] - pos[1];
	float dz = position[2] - pos[2];

	float r = dx*dx + dy*dy;
	r = sqrt(r);
	if(r < 0.00001) return;

	dx /= r;
	dy /= r;

	float mov_dist = 0.03;

	switch(dir)
	{
	case 0:
		position[0] -= dx * mov_dist;
		position[1] -= dy * mov_dist;
		break;
	case 1:
		position[0] += dx * mov_dist;
		position[1] += dy * mov_dist;
		break;
	case 2:
		position[0] += dy * mov_dist;
		position[1] -= dx * mov_dist;
		break;
	case 3:
		position[0] -= dy * mov_dist;
		position[1] += dx * mov_dist;
		break;
	}


}

